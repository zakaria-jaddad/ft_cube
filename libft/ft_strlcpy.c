#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;

	len = ft_strlen(src);
	if (dstsize == 0)
		return (len);
	--dstsize;
	while (*src && dstsize--)
		*dst++ = *src++;
	*dst = 0;
	return (len);
}
